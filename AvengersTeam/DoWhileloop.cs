    internal class DoWhileloop
    {

        public static void Main()
        {
            int i = 0;//initilization

            do
            {
                Console.WriteLine("Birds are flying");//statement
                i++;//increment 2
            }
            while (i<10);

            Console.ReadLine();

        }
    }
